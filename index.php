							<p>Krajowy System Elektroenergetyczny(KSE) z wielu względów wymaga ciągłego monitorowania i analizy.
							Jednym z powodów jest stan infrastruktury odpowiedzialnej za wytwarzanie energii elektrycznej. Niestety, w Polsce w przeważającej większości mamy do dyspozycji bloki o znacznym okresie eksploatacji. Szczególnie chodzi tu o bloki klasy 200 MW oraz 360 MW. 42 bloki mocy 200 MW pracują już ponad 200 tysięcy godzin a w miarę wzrostu czasu ich eksploatacji wzrasta również awaryjność przy jednoczesnym spadku dyspozycyjności. Fakty te mogą powodować poważne konsekwencje dla bezpieczeństwa energetycznego. Wzrost awaryjności bloków jest powodowany wyczerpywaniem się głównych modułów bloku ale również samym sposobem jego eksploatacji. W miarę wzrostu udziału energii z OZE w całkowitej produkcji zmienia się sposób wykorzystania bloków klasycznych. Energia z OZE ma pierwszeństwo w dostępie do sieci to znaczy, że klasyczne bloki muszą się jej podporządkować, pracując pod zmiennym obciążeniem. Taka sytuacja nie jest korzystna dla żadnej maszyny, ciągle zmienne obciążenia termiczne powodują szybszy wzrost awaryjności bloków oraz skracają ich żywotność. Dla bezpieczeństwa energetycznego naszego kraju jest wymagane aby decyzje odnośnie modernizacji starszych bloków lub ich wyłączeniu zapadły jak najszybciej, dodatkowo podjęcie jak najszybszej decyzji można uzasadnić niepewnością związaną z przyszłością energetyki jądrowej w Polsce. Działania takie wymagają ciągłej analizy parametrów pracy bloków i badania historii zmian tych parametrów.</p> <br/>
							
							<p>Sierpień 2017 roku przyniósł bardzo ciepłe dni, w skutek używania klimatyzacji zapotrzebowanie na energię elektryczną znacznie wzrosło. W połowie sierpnia obserwowaliśmy dużą liczbą gwałtownych burz i wiatrów nad naszym krajem a szczególnie w jego zachodniej części. Najbardziej niebezpiecznie było w dniach 10, 11 oraz 12 sierpnia, kilka osób zginęło podczas nawałnicy a kilkadziesiąt zostało rannych. Oprócz połamanych drzew i zerwanych dachów zniszczone lub uszkodzone zostały sieci elektroenergetyczne. Wiatr był tak silny, że przewracał słupy a drzewa zrywały linie przesyłowe i dystrybucyjne. W szczytowym  momencie bez energii elektrycznej było około 460 tys. odbiorców w tym 250 tys. było klientami Enei oraz 178 tys. Energi w dwóch pozostałych spółkach skala była znacznie mniejsza tzn. 26 tys. klientów PGE i 16 tys. Tauronu. Podkreślić należy, że mimo tak licznych wyłączeń cały system elektroenergetyczny pozostał stosunkowo stabilny. PSE S.A. umiejętnie przeorganizowało system zapewniając stabilny dostęp do energii elektrycznej dla osób niedotkniętych nawałnicami. Widzimy tutaj jak istotna jest analiza historii takich sytuacji dla naszego kraju. Dzięki niej możemy się przygotwać na wystąpienie podobnych katastrof w przyszłości. Po raz kolejny zatem widzimy jak istotne jest monitorowanie systemu elektroenergetycznego nie tylko w czasie rzeczywistym ale również historii jego zmian, oddaje to stosowność istnienia aplikacji gromadzących dane i przedstawiających je w różnych wariantach. </p>
